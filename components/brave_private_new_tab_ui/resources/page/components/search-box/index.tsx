interface Props {
  onSubmit?: (value: string, openNewTab: boolean) => unknown
}

function Search(props: Props) {
  const [value, setValue] = React.useState('')
  const inputRef = React.useRef<HTMLInputElement>(null)

  const onInputChange = (e: React.ChangeEvent<HTMLInputElement>) => {
    setValue(e.currentTarget.value)
  }

  const handleFormBoxClick = () => {
    inputRef.current && inputRef.current.focus()
  }

  const handleSubmit = (e: React.ChangeEvent<HTMLFormElement>) => {
    e.preventDefault()
    props.onSubmit?.(value, false)
  }

  const handleKeyDown = (e: React.KeyboardEvent<HTMLInputElement>) => {
    if (value === '') return

    if ((e.metaKey || e.ctrlKey) && e.key === 'Enter') {
      props.onSubmit?.(value, true)
    }
  }

  return (
    <Box>
      <BraveSearchLogo />
      <Form
        onSubmit={handleSubmit}
        onClick={handleFormBoxClick}
        role='search'
        aria-label='Brave'
      >
        <input
          ref={inputRef}
          onChange={onInputChange}
          onKeyDown={handleKeyDown}
          type='text'
          placeholder={getLocale('searchPlaceholderLabel')}
          value={value}
          autoCapitalize='off'
          autoComplete='off'
          autoCorrect='off'
          spellCheck='false'
          aria-label='Search'
          title='Search'
          aria-autocomplete='none'
          aria-haspopup='false'
          maxLength={2048}
          autoFocus
        />
        <IconButton data-test-id='submit_button' aria-label='Submit'>
          <svg
            width='20'
            height='20'
            fill='none'
            xmlns='http://www.w3.org/2000/svg'
          >
            <path
              fillRule='evenodd'
              clipRule='evenodd'
              d='M8 16a8 8 0 1 1 5.965-2.67l5.775 5.28a.8.8 0 1 1-1.08 1.18l-5.88-5.375A7.965 7.965 0 0 1 8 16Zm4.374-3.328a.802.802 0 0 0-.201.18 6.4 6.4 0 1 1 .202-.181Z'
              fill='url(#search_icon_gr)'
            />
            <defs>
              <linearGradient
                id='search_icon_gr'
                x1='20'
                y1='20'
                x2='-2.294'
                y2='3.834'
                gradientUnits='userSpaceOnUse'
              >
                <stop stopColor='#BF14A2' />
                <stop offset='1' stopColor='#F73A1C' />
              </linearGradient>
            </defs>
          </svg>
        </IconButton>
      </Form>
    </Box>
  )
}

export default Search
